package javatest1.dataandvar;

public class DataType {

    /**
     * 标识符
     * 数字不能开头
     * 不能使用关键字
     */

    /**
     * 命名规范
     * 1.包名：小写
     * 2.类名和接口名：驼峰式（DataType）
     * 3.变量名和函数名 studyJava（）
     * 4.常量：大写
     */
    /**
     * 数据类型
     * 1.基本数据类型
     * 2.引用型
     */

    byte a;
    short b;
    int c;
    long d = 3l;
    float e = 3.0f;
    double f = 3.9d;
    char g;
    boolean h;

    //常量
    private static final int STUDY_TIME = 1000;

    /**
     * 整数
     * 1.十进制
     * 2.二进制 0b1001
     * 3.八进制 0-7 034
     * 4.十六进制 0-9 A-F 0x56
     */

    public static void main(String[] args) {
        System.out.println("王天锐");
    }
}
